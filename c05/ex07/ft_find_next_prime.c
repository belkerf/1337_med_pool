int	ft_find_next_prime(int nb)
{
	int	i;

	if (nb <= 1)
		return (2);
	i = nb - 1;
	while (i > 1)
	{
		if (nb % i == 0)
			nb = ft_find_next_prime(nb + 1);
		i--;
	}
	return (nb);
}
