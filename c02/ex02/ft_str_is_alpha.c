int	ft_str_is_alpha(char *str)
{
	while (*str)
	{
		if ((*str <= 'Z' && *str >= 'A') || (*str >= 'a' && *str <= 'z'))
		{
			str++;
		}
		else
			return (0);
	}
	return (1);
}
