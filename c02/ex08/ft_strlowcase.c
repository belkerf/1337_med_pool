char	*ft_strlowcase(char *str)
{
	char	*ma;
	int		l;

	ma = str;
	l = 'a' - 'A';
	while (*ma)
	{
		if (*ma >= 'A' && *ma <= 'Z')
		{
			*ma = *ma + l;
		}
		ma++;
	}
	return (str);
}
