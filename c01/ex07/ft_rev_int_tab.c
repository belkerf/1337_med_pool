void	ft_rev_int_tab(int *tab, int size)
{
	int	arr[1000];
	int	i;

	i = 0;
	while (i < size)
	{
		arr[i] = *tab;
		tab++;
		i++;
	}
	tab--;
	i = 0;
	while (i < size)
	{
		*tab = arr[i];
		tab--;
		i++;
	}
}
